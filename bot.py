import ccxt
import pandas as pd
import ta
import requests
import time
import os
from dotenv import load_dotenv

# 🔐 Laad .env
load_dotenv()

TOKEN = os.getenv("TELEGRAM_TOKEN")
CHAT_ID = os.getenv("CHAT_ID")

exchange = ccxt.kraken()
last_signal = None


def send_message(text):
    url = f"https://api.telegram.org/bot{TOKEN}/sendMessage"

    try:
        requests.post(url, data={
            "chat_id": CHAT_ID,
            "text": text
        })
        print("Bericht gestuurd!")
    except Exception as e:
        print("Telegram fout:", e)


# 🔹 Data ophalen + indicators
def get_data(timeframe):
    ohlc = exchange.fetch_ohlcv("SOL/USD", timeframe=timeframe, limit=100)
    df = pd.DataFrame(ohlc, columns=['t','o','h','l','c','v'])

    df['rsi'] = ta.momentum.RSIIndicator(df['c']).rsi()
    df['ema9'] = ta.trend.EMAIndicator(df['c'], window=9).ema_indicator()
    df['ema21'] = ta.trend.EMAIndicator(df['c'], window=21).ema_indicator()

    return df


    # 🔹 BETERE support/resistance (swing points)
def get_support_resistance(df):
    highs = []
    lows = []

    df = df.tail(50)  # alleen recente candles

    for i in range(2, len(df)-2):
        if df['h'].iloc[i] > df['h'].iloc[i-1] and df['h'].iloc[i] > df['h'].iloc[i+1]:
            highs.append(df['h'].iloc[i])
        if df['l'].iloc[i] < df['l'].iloc[i-1] and df['l'].iloc[i] < df['l'].iloc[i+1]:
            lows.append(df['l'].iloc[i])

    resistance = max(highs[-5:]) if highs else df['h'].max()
    support = min(lows[-5:]) if lows else df['l'].min()

    return support, resistance


def run_bot():
    global last_signal

    try:
        ticker = exchange.fetch_ticker("SOL/USD")
        price = ticker["last"]

        # 🔹 Data ophalen
        df_1h = get_data("1h")
        df_4h = get_data("4h")
        df_1d = get_data("1d")

        # 🔹 Indicators
        rsi_1h = df_1h['rsi'].iloc[-1]

        ema9_4h = df_4h['ema9'].iloc[-1]
        ema21_4h = df_4h['ema21'].iloc[-1]

        ema9_1d = df_1d['ema9'].iloc[-1]
        ema21_1d = df_1d['ema21'].iloc[-1]

        # 🔹 Support/Resistance (4H = beste balans)
        support, resistance = get_support_resistance(df_4h)

        # 🔹 Zones (1%)
        zone_size = 0.01
        support_zone = (support * (1 - zone_size), support * (1 + zone_size))
        resistance_zone = (resistance * (1 - zone_size), resistance * (1 + zone_size))

        # 🔹 Trend
        uptrend = ema9_4h > ema21_4h and ema9_1d > ema21_1d
        downtrend = ema9_4h < ema21_4h and ema9_1d < ema21_1d

        # 🔹 Beslislogica (confluence)
        if (rsi_1h < 30 and uptrend and
            support_zone[0] <= price <= support_zone[1]):
            signal = "BUY"

        elif (rsi_1h > 70 and downtrend and
              resistance_zone[0] <= price <= resistance_zone[1]):
            signal = "SELL"

        else:
            signal = "HOLD"

        message = (
            f"SOL/USD\n"
            f"Price: {price}\n\n"
            f"RSI (1H): {round(rsi_1h,2)}\n\n"
            f"Support Zone: {round(support_zone[0],2)} - {round(support_zone[1],2)}\n"
            f"Resistance Zone: {round(resistance_zone[0],2)} - {round(resistance_zone[1],2)}\n\n"
            f"Trend: {'UP' if uptrend else 'DOWN' if downtrend else 'SIDEWAYS'}\n\n"
            f"Signal: {signal}"
        )

        # 🔹 Alleen bij verandering sturen
        if signal != last_signal:
            send_message(message)
            last_signal = signal

        print("Signal:", signal)

    except Exception as e:
        print("Fout:", e)


while True:
    run_bot()
    print("Wacht 30 minuten...")
    time.sleep(1800)