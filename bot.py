import ccxt
import pandas as pd
import ta
import requests
import time
import os
from dotenv import load_dotenv

# 🔐 Laad .env
load_dotenv()
TOKEN = os.getenv("TELEGRAM_TOKEN")
CHAT_ID = os.getenv("CHAT_ID")

exchange = ccxt.kraken()

SYMBOLS = ["BTC/USD", "ETH/USD", "SOL/USD"]

UPDATE_INTERVAL = 14400  # 4 uur heartbeat

last_signals = {}
last_update_time = {}

for s in SYMBOLS:
    last_signals[s] = None
    last_update_time[s] = 0

# Telegram functie
def send_message(text):
    url = f"https://api.telegram.org/bot{TOKEN}/sendMessage"
    try:
        requests.post(url, data={"chat_id": CHAT_ID, "text": text}, timeout=10)
        print("Bericht gestuurd!")
    except Exception as e:
        print("Telegram fout:", e)

# Data ophalen
def get_data(symbol, timeframe="1h", limit=200):
    ohlc = exchange.fetch_ohlcv(symbol, timeframe=timeframe, limit=limit)
    df = pd.DataFrame(ohlc, columns=['t','o','h','l','c','v'])

    df['ema50'] = ta.trend.EMAIndicator(df['c'], window=50).ema_indicator()
    df['ema200'] = ta.trend.EMAIndicator(df['c'], window=200).ema_indicator()
    df['rsi'] = ta.momentum.rsi(df['c'], window=14)
    df['vol_avg'] = df['v'].rolling(20).mean()

    return df

# Strategie
def generate_signal(df):
    if len(df) < 200:
        return None

    last = df.iloc[-1]
    recent = df.iloc[-100:]

    price = last['c']
    rsi = last['rsi']
    volume = last['v']
    avg_volume = last['vol_avg']

    ema50 = last['ema50']
    ema200 = last['ema200']

    # 🔹 Trend
    if ema50 > ema200:
        trend = "UP"
    elif ema50 < ema200:
        trend = "DOWN"
    else:
        trend = "SIDEWAYS"

    # 🔹 Support & Resistance
    support = recent['l'].min()
    resistance = recent['h'].max()

    # 🔹 Volume confirmation
    volume_ok = volume > avg_volume

    signal = None

    # BUY
    if (
        trend == "UP" and
        40 <= rsi <= 50 and
        price > ema50 and
        volume_ok and
        price > support
    ):
        signal = "BUY"

    # SELL
    elif (
        trend == "DOWN" and
        50 <= rsi <= 60 and
        price < ema50 and
        volume_ok and
        price < resistance
    ):
        signal = "SELL"

    return signal, trend, support, resistance, volume_ok

