import ccxt
import pandas as pd
import ta
import requests
import time
import os
from dotenv import load_dotenv

# 🔐 Laad .env
load_dotenv()
TOKEN = os.getenv("TELEGRAM_TOKEN")
CHAT_ID = os.getenv("CHAT_ID")

exchange = ccxt.kraken()

SYMBOLS = ["BTC/USD", "ETH/USD", "SOL/USD"]

UPDATE_INTERVAL = 14400  # 4 uur heartbeat

last_signals = {}
last_update_time = {}

for s in SYMBOLS:
    last_signals[s] = None
    last_update_time[s] = 0

# =========================
# 📩 TELEGRAM
# =========================
def send_message(text):
    url = f"https://api.telegram.org/bot{TOKEN}/sendMessage"
    try:
        requests.post(url, data={"chat_id": CHAT_ID, "text": text}, timeout=10)
        print("Bericht gestuurd!")
    except Exception as e:
        print("Telegram fout:", e)

# =========================
# 📊 DATA
# =========================
def get_data(symbol, timeframe="1h", limit=200):
    ohlc = exchange.fetch_ohlcv(symbol, timeframe=timeframe, limit=limit)
    df = pd.DataFrame(ohlc, columns=['t','o','h','l','c','v'])

    df['ema50'] = ta.trend.EMAIndicator(df['c'], window=50).ema_indicator()
    df['ema200'] = ta.trend.EMAIndicator(df['c'], window=200).ema_indicator()
    df['rsi'] = ta.momentum.rsi(df['c'], window=14)
    df['vol_avg'] = df['v'].rolling(20).mean()

    return df

# =========================
# 🧠 STRATEGIE
# =========================
def generate_signal(df):
    if len(df) < 200:
        return None

    last = df.iloc[-1]
    prev = df.iloc[-2]

    # =========================
    # 🔹 MARKET STRUCTURE (50 candles)
    # =========================
    structure_df = df.iloc[-50:]

    highs = structure_df['h']
    lows = structure_df['l']

    recent_high = highs.max()
    recent_low = lows.min()

    prev_high = highs[:-1].max()
    prev_low = lows[:-1].min()

    price = last['c']
    prev_price = prev['c']

    # MSB detectie
    if price > prev_high and prev_price <= prev_high:
        msb = "BULLISH"
    elif price < prev_low and prev_price >= prev_low:
        msb = "BEARISH"
    else:
        msb = "NONE"

    # =========================
    # 🔹 TREND
    # =========================
    ema50 = last['ema50']
    ema200 = last['ema200']

    if ema50 > ema200:
        trend = "UP"
    elif ema50 < ema200:
        trend = "DOWN"
    else:
        trend = "SIDEWAYS"

    # =========================
    # 🔹 RSI TREND
    # =========================
    rsi = last['rsi']
    prev_rsi = prev['rsi']

    rsi_up = rsi > prev_rsi
    rsi_down = rsi < prev_rsi

    bullish_rsi = (40 <= rsi <= 55) and rsi_up
    bearish_rsi = (45 <= rsi <= 60) and rsi_down

    # =========================
    # 🔹 VOLUME
    # =========================
    volume = last['v']
    avg_volume = last['vol_avg']

    volume_ok = volume > avg_volume

    # =========================
    # 🔹 SUPPORT / RESISTANCE
    # =========================
    support = recent_low
    resistance = recent_high

    # =========================
    # 🔹 SIGNAL
    # =========================
    signal = "HOLD"

    if (
        msb == "BULLISH" and
        trend == "UP" and
        bullish_rsi and
        volume_ok
    ):
        signal = "BUY"

    elif (
        msb == "BEARISH" and
        trend == "DOWN" and
        bearish_rsi and
        volume_ok
    ):
        signal = "SELL"

    # =========================
    # 🎯 TARGET & SL
    # =========================
    if signal == "BUY":
        target = resistance
        stop_loss = support

    elif signal == "SELL":
        target = support
        stop_loss = resistance

    else:
        target = None
        stop_loss = None

    return (
        signal,
        trend,
        msb,
        support,
        resistance,
        volume_ok,
        rsi,
        target,
        stop_loss
    )

# =========================
# 🤖 BOT LOOP
# =========================
def run_bot():
    for symbol in SYMBOLS:
        try:
            ticker = exchange.fetch_ticker(symbol)
            price = ticker['last']

            df = get_data(symbol)

            result = generate_signal(df)
            if result is None:
                continue

            signal, trend, msb, support, resistance, volume_ok, rsi, target, stop_loss = result

            current_time = time.time()
            time_passed = current_time - last_update_time[symbol]

            # =========================
            # 🚨 NIEUW SIGNAAL
            # =========================
            if signal != last_signals[symbol] and signal != "HOLD":

                message = (
                    f"🚨 NIEUW SIGNAAL\n"
                    f"🔔 {symbol}\n"
                    f"Prijs: {price}\n"
                    f"Trend: {'📈' if trend=='UP' else '📉' if trend=='DOWN' else '↔️'}\n"
                    f"MSB: {msb}\n"
                    f"RSI: {round(rsi,2)}\n"
                    f"Volume: {'✅' if volume_ok else '❌'}\n"
                    f"Support: {round(support,2)}\n"
                    f"Resistance: {round(resistance,2)}\n"
                    f"🎯 Target: {round(target,2) if target else '—'}\n"
                    f"🛑 Stop Loss: {round(stop_loss,2) if stop_loss else '—'}\n"
                    f"Signaal: {signal}"
                )

                send_message(message)

                last_signals[symbol] = signal
                last_update_time[symbol] = current_time

            # =========================
            # 🤖 HEARTBEAT
            # =========================
            elif time_passed >= UPDATE_INTERVAL:

                message = (
                    f"🤖 BOT STATUS UPDATE (4H)\n"
                    f"🔔 {symbol}\n"
                    f"Prijs: {price}\n"
                    f"Trend: {'📈' if trend=='UP' else '📉' if trend=='DOWN' else '↔️'}\n"
                    f"MSB: {msb}\n"
                    f"RSI: {round(rsi,2)}\n"
                    f"Volume: {'✅' if volume_ok else '❌'}\n"
                    f"Support: {round(support,2)}\n"
                    f"Resistance: {round(resistance,2)}\n"
                    f"Status: Bot draait correct ✅\n"
                    f"Laatste signaal: {last_signals[symbol]}"
                )

                send_message(message)
                last_update_time[symbol] = current_time

            print(f"{symbol} | {price} | Trend: {trend} | MSB: {msb} | Signal: {signal}")

        except Exception as e:
            print(f"Fout bij {symbol}: {e}")

        time.sleep(1)

# =========================
# 🔁 LOOP
# =========================
while True:
    run_bot()
    print("Scan voltooid. Wacht 1 uur...")
    time.sleep(3600)